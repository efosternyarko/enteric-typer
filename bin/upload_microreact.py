#!/usr/bin/env python3
"""Post enteric-typer results table (+ optional Newick tree) to Microreact v2 API."""

import argparse
import csv
import datetime
import json
import os
import sys
import time

try:
    import requests
except ImportError:
    sys.exit("ERROR: requests library not installed. Run: pip install requests")

MICROREACT_API = "https://microreact.org/api/projects/create"


def read_tsv(tsv_path: str) -> tuple[str, list[str]]:
    """Read TSV, rename 'sample' → 'id' (required by Microreact), return (blob, columns)."""
    with open(tsv_path) as fh:
        content = fh.read()
    content_renamed = content.replace("sample\t", "id\t", 1)

    with open(tsv_path) as fh:
        cols = csv.DictReader(fh, delimiter="\t").fieldnames or []
    cols_renamed = ["id" if c == "sample" else c for c in cols]

    return content_renamed, cols_renamed


def read_tree(tree_path: str | None) -> str | None:
    """Read Newick tree file if it exists and is non-empty."""
    if not tree_path:
        return None
    p = tree_path.strip()
    if p in ("NO_FILE", "", "null") or not os.path.isfile(p):
        return None
    content = open(p).read().strip()
    return content if content and content != "" else None


def build_payload(tsv_path: str, tree_path: str | None, project_name: str) -> dict:
    blob, columns = read_tsv(tsv_path)
    newick        = read_tree(tree_path)
    timestamp     = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")

    payload: dict = {
        "version": 1,
        "schema": "https://microreact.org/schema/v1.json",
        "meta": {
            "name":        f"{project_name} — {timestamp}",
            "description": "Auto-generated by enteric-typer pipeline",
        },
        "files": {
            "data-file-1": {
                "id":     "data-file-1",
                "name":   os.path.basename(tsv_path),
                "format": "text/tab-separated-values",
                "blob":   blob,
            }
        },
        "datasets": {
            "dataset-1": {
                "file":        "data-file-1",
                "idFieldName": "id",
            }
        },
        "tables": {
            "table-1": {
                "dataset": "dataset-1",
                "file":    "data-file-1",
                "title":   "Genotyping results",
                "columns": [{"field": c} for c in columns],
            }
        },
    }

    if newick:
        payload["files"]["tree-file-1"] = {
            "id":     "tree-file-1",
            "name":   "tree.nwk",
            "format": "text/x-nh",
            "blob":   newick,
        }
        payload["trees"] = {
            "tree-1": {
                "id":      "tree-1",
                "file":    "tree-file-1",
                "name":    "IQ-TREE phylogeny",
                "dataset": "dataset-1",
            }
        }

    return payload


def upload(tsv_path: str, tree_path: str | None, project_name: str, token: str) -> str:
    payload = build_payload(tsv_path, tree_path, project_name)
    headers = {
        "Access-Token":  token,
        "Content-Type":  "application/json; charset=utf-8",
    }
    time.sleep(1)
    resp = requests.post(MICROREACT_API, headers=headers,
                         data=json.dumps(payload), timeout=60)
    if resp.status_code in (200, 201):
        result = resp.json()
        return result.get("url") or f"https://microreact.org/project/{result.get('id', 'unknown')}"
    raise RuntimeError(f"Microreact API returned {resp.status_code}: {resp.text[:500]}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Upload results to Microreact")
    parser.add_argument("--input",   required=True,  help="Aggregated results TSV")
    parser.add_argument("--tree",    default=None,    help="IQ-TREE Newick .treefile (optional)")
    parser.add_argument("--project", required=True,  help="Microreact project name")
    parser.add_argument("--token",   default=None,
                        help="Microreact API token (or set MICROREACT_TOKEN env var)")
    parser.add_argument("--output",  required=True,  help="File to write project URL to")
    args = parser.parse_args()

    token = args.token or os.environ.get("MICROREACT_TOKEN")
    if not token:
        sys.exit("ERROR: Microreact API token not provided.\n"
                 "Set MICROREACT_TOKEN env var or use: nextflow secrets set MICROREACT_TOKEN <token>")

    print(f"Uploading {args.input} to Microreact…", file=sys.stderr)
    if args.tree and os.path.isfile(args.tree):
        print(f"  including tree: {args.tree}", file=sys.stderr)

    try:
        url = upload(args.input, args.tree, args.project, token)
    except Exception as e:
        with open(args.output, "w") as fh:
            fh.write(f"UPLOAD_FAILED: {e}\n")
        sys.exit(f"ERROR: Microreact upload failed: {e}")

    with open(args.output, "w") as fh:
        fh.write(url + "\n")

    print(f"Microreact project URL: {url}", file=sys.stderr)


if __name__ == "__main__":
    main()
